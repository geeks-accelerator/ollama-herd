# ----------------------------------------------------------------------------
# Summary cache — content-addressed, persistent across sessions
# ----------------------------------------------------------------------------


class SummaryCache:
    """SQLite-backed content-addressed summary cache.

    Key: (content_sha256, strategy_name, strategy_version).  Returning
    the SAME summary bytes for the SAME input content is what preserves
    mlx prefix-cache stability across sessions.

    Thread-safe for concurrent reads; writes are single-threaded via
    a connection-per-call pattern (SQLite's default-serialized mode).
    """

    def __init__(self, db_path: Path):
        self.db_path = db_path
        self.db_path.parent.mkdir(parents=True, exist_ok=True)
        self._init_schema()

    def _conn(self) -> sqlite3.Connection:
        c = sqlite3.connect(self.db_path, timeout=5.0)
        c.row_factory = sqlite3.Row
        return c

    def _init_schema(self) -> None:
        with self._conn() as c:
            c.execute("""
                CREATE TABLE IF NOT EXISTS summaries (
                    content_hash TEXT NOT NULL,
                    strategy TEXT NOT NULL,
                    strategy_version TEXT NOT NULL,
                    summary TEXT NOT NULL,
                    original_tokens INTEGER NOT NULL,
                    summary_tokens INTEGER NOT NULL,
                    curator_model TEXT NOT NULL,
                    created_at REAL NOT NULL,
                    hit_count INTEGER NOT NULL DEFAULT 0,
                    PRIMARY KEY (content_hash, strategy, strategy_version)
                )
            """)
            c.execute("""
                CREATE INDEX IF NOT EXISTS idx_summaries_created_at
                ON summaries(created_at)
            """)

    def get(
        self, content_hash: str, strategy: str, version: str = STRATEGY_VERSION,
    ) -> str | None:
        with self._conn() as c:
            row = c.execute(
                "SELECT summary FROM summaries "
                "WHERE content_hash = ? AND strategy = ? AND strategy_version = ?",
                (content_hash, strategy, version),
            ).fetchone()
            if row is None:
                return None
            # Bump hit count (best-effort; don't block caller on failure)
            with contextlib.suppress(sqlite3.OperationalError):
                c.execute(
                    "UPDATE summaries SET hit_count = hit_count + 1 "
                    "WHERE content_hash = ? AND strategy = ? AND strategy_version = ?",
                    (content_hash, strategy, version),
                )
            return row["summary"]

    def put(
        self,
        content_hash: str,
        strategy: str,
        summary: str,
        original_tokens: int,
        summary_tokens: int,
        curator_model: str,
        version: str = STRATEGY_VERSION,
    ) -> None:
        with self._conn() as c:
            c.execute(
                "INSERT OR REPLACE INTO summaries "
                "(content_hash, strategy, strategy_version, summary, "
                "original_tokens, summary_tokens, curator_model, created_at, hit_count) "
                "VALUES (?, ?, ?, ?, ?, ?, ?, ?, 0)",
                (
                    content_hash, strategy, version, summary,
                    original_tokens, summary_tokens, curator_model, time.time(),
                ),
            )

    def stats(self) -> dict:
        """Cache size / hit distribution for dashboard exposure."""
        with self._conn() as c:
            row = c.execute(
                "SELECT COUNT(*) as n_entries, "
                "SUM(original_tokens) as total_original, "
                "SUM(summary_tokens) as total_summary, "
                "SUM(hit_count) as total_hits "
                "FROM summaries"
            ).fetchone()
            return {
                "entries": row["n_entries"] or 0,
                "total_original_tokens": row["total_original"] or 0,
                "total_summary_tokens": row["total_summary"] or 0,
                "total_hits": row["total_hits"] or 0,
            }


