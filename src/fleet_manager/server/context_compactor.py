"""Context Hygiene Compactor — shrinks bloated conversation history before
main-model inference, so local LLMs with limited effective-context can still
reason coherently.

Architecture rationale — see `docs/research/...` in the private repo for the
full design doc.  Summary:

  - Local LLMs (e.g. Qwen3-Coder-480B on Mac Studio) have effective agent
    context of ~20-40K tokens before attention degrades.  Hosted Claude
    tolerates much more because Anthropic pays the attention-compute cost.
  - Claude Code CLI tools were designed for hosted-LLM economics:
    ``Read`` dumps whole files, ``Bash`` dumps full stdout, etc.  For
    sessions of 5+ turns this quickly exceeds local effective context.
  - Solution: middleware that replaces bloated ``tool_result`` blocks with
    faithful summaries BEFORE they reach the main model.  Summaries are
    generated by a smaller curator model (e.g. gpt-oss:120b on the same
    fleet), cached by content hash (sticky across sessions), and marked
    explicitly so the agent knows what's compact vs verbatim.

Key invariants — DO NOT VIOLATE:

  1. **Cache stability.** Compacted prefix bytes must be identical across
     requests for the same underlying content, or we bust mlx's prefix
     cache and lose the 10-100× warm-turn speedup.  Enforced via:
       - Content-addressed cache (SHA-256 of raw content → summary)
       - Deterministic curator calls (temperature=0)
       - Sticky summaries (once cached, never regenerated until strategy
         version bumps)

  2. **Never compact cached prefix content.**  System prompt and tool
     definitions are pinned in the prefix cache.  Compacting them would
     bust the cache for every subsequent turn.  Only the message history
     (specifically: old tool_result blocks) is eligible.

  3. **Preserve recency.**  The last N turns (default 3) are passed
     through verbatim.  These are the model's active reasoning context
     and compacting them damages quality.

  4. **Preserve tool_use (assistant side).**  ``tool_use`` blocks inside
     assistant messages are the model's own output; compacting them
     breaks trace continuity and may confuse the agent about its own
     prior decisions.  Only tool RESULTS (the user-role reply that
     follows a tool_use) are compactable.

  5. **Fail open.**  If the curator fails or times out, pass the
     original content through unchanged and log the failure.  Bad
     compaction is worse than no compaction.

# EXTRACTION SEAM (recorded 2026-04-24):
# - Fleet-manager dependencies (at runtime, not import time):
#     * ``CuratorSelector.select()`` reads ``NodeState``-like objects but only
#       touches ``.ollama.models_loaded[].name`` and ``.node_id`` — duck-typed.
#       Extraction would replace with any shape that exposes that surface.
#     * The ``curator_selector`` + ``resolve_curator_context`` callbacks are
#       LATE-BOUND — constructed by ``app.py`` when the compactor is wired up.
#       The module itself has no fleet_manager imports.
#     * Lazy import inside ``CuratorSelector._eligible()`` for
#       ``fleet_manager.server.model_knowledge`` — would need to be
#       parameterized or inlined on extraction.
# - External dependencies: stdlib + sqlite3 + logging.
# - Public surface to preserve if extracted:
#     STRATEGY_VERSION, STRATEGIES_BY_TOOL (Strategy dataclass + tool→strategy map)
#     CuratorProtocol (Protocol), OllamaCurator (example impl)
#     SummaryCache (SQLite-backed)
#     CompactionReport, ContextCompactor, CuratorSelector
# - Critical invariant: byte-stable summaries across runs.  See module
#   docstring invariant (1).  Any extraction MUST preserve the
#   deterministic-curator-call + content-hash-keyed-cache contract.
# - Medium coupling; extract together with ``context_management.py`` as a
#   layer-1 + layer-2 pair for a cohesive "context management" package.
#   Remove the ``model_knowledge`` import (or make it a pluggable predicate)
#   to sever the last fleet-manager tie.
"""

